export const TEMPLATE_COUNT = TEMPLATE_DATABASE.length;

// Export helper functions for filtering
export const getTemplatesByCategory = (category: string) => 
  TEMPLATE_DATABASE.filter(t => t.category === category);

export const getTemplatesByIndustry = (industry: string) => 
  TEMPLATE_DATABASE.filter(t => t.industry === industry);

export const getTemplatesByActivity = (activity: string) => 
  TEMPLATE_DATABASE.filter(t => t.activity === activity);

export const searchTemplates = (query: string) => 
  TEMPLATE_DATABASE.filter(t => 
    t.name.toLowerCase().includes(query.toLowerCase()) ||
    t.description.toLowerCase().includes(query.toLowerCase()) ||
    t.tags?.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
  );

export const getPopularTemplates = () => 
  TEMPLATE_DATABASE.filter(t => t.popular);

export const getNewTemplates = () => 
  TEMPLATE_DATABASE.filter(t => t.new);
